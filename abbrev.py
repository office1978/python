# -*- encoding: utf-8 -*-
k = {"К":"Комитет", "Г":"Государственной", "Б":"Безопасности", "Р":"Российская", "Ф":"Федерация", "А":"Автоматизированная", "С":"Система", "У":"Управления"}
d=input("Введите аббревиатуру: ")
d=d.upper()
s=str()
for i in d:
    if i in k:
        s=s+k[i]+" "
    else:
        print ("Вы ошиблись. Попробуйте снова.")
        break
print (s)


    



